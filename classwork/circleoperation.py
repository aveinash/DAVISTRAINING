# program to find out area and perimeter of circle

# creating variable to store radius of the circle
radius = 5

# displaying radius of the circle
print("------Circle------")
print("Radius of circle:", radius)

# calculating area of circle
area = 3.14 * radius * radius
print("Area of circle:", area)

# calculating perimeter (circumference) of circle
perimeter = 2 * 3.14 * radius
print("Perimeter of circle:", perimeter)
